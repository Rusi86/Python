# programa que calcule dos numeros operando con los 7 operadores vistos en clase. ¿Cómo puedes forzar que el resultado de la división tnga dos decimales?

print("hola")

valor1=int(input("Introduce un valor:"))
valor2=int(input("Introduce otro valor:"))


print("Suma")
total=valor1 + valor2
print(f"La suma de {valor1} y {valor2} es:", total)


print("Resta")
total=valor1 - valor2
print(f"La resta de {valor1} y {valor2} es:", total)


print("Multiplicación")
total=valor1 * valor2
print(f"La multiplicación de {valor1} y {valor2} es:", total)


print("División")
total=valor1 / valor2
print(f"La división de {valor1} y {valor2} es:", total)


print("Potencia")
total=valor1 ** valor2
print(f"El exponente de {valor1} y {valor2} es:", total)


print("División entera")
total=valor1 // valor2
print(f"La división entera de {valor1} y {valor2} es:", total)